// Record a sale
exports.recordProductSale = async (req, res) => {
  try {
    const { quantity, salePrice } = req.body;
    
    if (!quantity || !salePrice) {
      return res.status(400).json({
        success: false,
        error: 'Please provide quantity and sale price'
      });
    }
    
    const product = await Product.findById(req.params.id);
    
    if (!product) {
      return res.status(404).json({
        success: false,
        error: 'Product not found'
      });
    }
    
    // Check if there's enough stock
    if (product.stock < quantity) {
      return res.status(400).json({
        success: false,
        error: 'Not enough stock available'
      });
    }
    
    // Update stock
    product.stock -= quantity;
    
    // Record the sale using the model method
    await product.recordSale(quantity, salePrice);
    
    res.status(200).json({
      success: true,
      data: product
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      error: 'Server Error'
    });
  }
};

